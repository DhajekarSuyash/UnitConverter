package data.conversion.UnitConverter.operations;

public class MultiplicationFactor {

    public static double multiplyByTen(double inputValue) {
        return inputValue * 10;
    }

    public static double multiplyByHundred(double inputValue) {
        return inputValue * 100;
    }

    public static double multiplyByThousand(double inputValue) {
        return inputValue * 1000;
    }

    public static double multiplyByTenthousand(double inputValue) {
        return inputValue * 10000;
    }

    public static double multiplyByHundreadThousand(double inputValue) {
        return inputValue * 100000;
    }
}
